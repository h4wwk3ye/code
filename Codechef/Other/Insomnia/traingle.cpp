void pr () {
    cout << "\n";
}

template < typename T, typename... ARGS > void pr (const T& t, ARGS... args) {
    cout << t << " ";
    pr (args...);
}

// clang-format on

signed main () {
	ios_base::sync_with_stdio (false), cin.tie (nullptr);
	int tc;
	cin >> tc;
	while (tc--) {
		int n;
		cin >> n;
		int ans = 8;
		if (n == 1) ans = 3;
		if (n == 2) ans = 4;
		if (n == 3) ans = 5;
		if (n == 4) ans = 6;
		if (n == 5) ans = 7;
		pr (ans);
	}
	return 0;
}
