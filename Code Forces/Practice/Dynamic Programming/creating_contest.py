import sys

inf = float("inf")
mod = 1000000007


def get_array(): return list(map(int, sys.stdin.readline().split()))


def get_ints(): return map(int, sys.stdin.readline().split())


def input(): return sys.stdin.readline()

# ///==========MAIN=============///


def main():
    n = int(input())
    arr = get_array()
    dp = [1]*n
    for i in range(1, n):
        if arr[i] <= 2*arr[i-1]:
            dp[i] = 1+dp[i-1]

    print(max(dp))


if __name__ == "__main__":
    main()
